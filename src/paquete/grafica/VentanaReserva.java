    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        // BOTON RESERVAR
        // Al presionar el boton reservar almacena todo la informacion, recalcula el precio*asientos vuelo, toma el codigo del vuelo
        String codTicketIda = this.jTextField3.getText();
        String codTicketVuelta = this.jTextField4.getText();
        String codVueloIda = obtenerVuelo(jTable1,codTicketIda);
        String codVueloVuelta = obtenerVuelo(jTable2,codTicketVuelta);
        //
        float precioIda = obtenerMonto(jTable1,codTicketIda);
        float precioVuelta = obtenerMonto(jTable2,codTicketVuelta);
        
         //INICIALIZO LA RESERVA
        reserva = new Reserva(usuario1.getUsuarioSesion().getCodigoCliente(),(Integer)this.jSpinner1.getValue());
        reserva.setCodigoTicketIda(codTicketIda);
        reserva.setCodigoTicketVuelta(codTicketVuelta);
        reserva.setCodigovueloIda(codVueloIda);
        reserva.setCodigovueloRetorno(codVueloVuelta);
        reserva.setCodigoCliente(usuario1.getUsuarioSesion().getCodigoCliente());
        reserva.setNumeroPasajeros((Integer)this.jSpinner1.getValue());
        float monto = precioIda*reserva.getNumeroPasajeros() + precioVuelta*reserva.getNumeroPasajeros();
        reserva.setMontoTotal(monto);
        this.refGReserva.ValidarDatosReserva(reserva);
        this.refGReserva.Ingresar(reserva);
        
        
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private String obtenerVuelo(JTable tabla, String ticketIda) {
        String codigo = "";
        
        //filas de la tabla
        for (int i = 0; i < tabla.getRowCount(); i++) 
        {  // Loop through the rows
            DefaultTableModel mod = (DefaultTableModel)tabla.getModel();
            String ticket =  mod.getValueAt(i, 6).toString(); 
            if (ticket.equals(ticketIda)){
                // para obtener el codigo del vuelo de la tabla para posteriormente ser reflejada en la siguiente pagina
                codigo = mod.getValueAt(i, 5).toString(); 
                break;
            }
        }
        return codigo;
    }
    
    private float obtenerMonto(JTable tabla, String ticketIda) {
        float codigo = 0.0f;
        
        //Obtiene el monto de la tabla asignada

        for (int i = 0; i < tabla.getRowCount(); i++) 
        {  // Loop through the rows
            DefaultTableModel mod = (DefaultTableModel)tabla.getModel();
            // para obtener el precio que muestra la tabla para posteriormente ser reflejada en la siguiente pagina
            String ticket =  mod.getValueAt(i, 6).toString(); 
            if (ticket.equals(ticketIda)){
                codigo = Float.parseFloat(mod.getValueAt(i, 1).toString()); 
                break;
            }
        }
        return codigo;
    }
    
