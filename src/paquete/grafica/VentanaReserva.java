    public void CargarDatosTablaIda(Vuelo[] lista)
    {
        int filas = this.jTable1.getRowCount();
        for (int i = 0; i < filas; i++) 
        {
            modelo.removeRow(0);
        }

        String[] datos = new String[9];
        for (int i = 0; i < lista.length; i++) 
        {
            if (lista[i] != null) 
            {
                datos[0] = lista[i].getFechaVuelo();
                datos[1] = String.valueOf(lista[i].getMontoFinal());
                datos[2] = lista[i].getOrigen();
                datos[3] = lista[i].getDestino();
                datos[4] = lista[i].getHora();
                datos[5] = lista[i].getCodigoVuelo();
                datos[6] = lista[i].getTicket();
                datos[7] = lista[i].getMatricula();
                
                if (lista[i] instanceof VEconomico) 
                {
                    datos[8] = "ECONOMICO";
                } 
                else if (lista[i] instanceof VPremium) 
                {
                    datos[8] = "PREMIUM";
                } 
                else if (lista[i] instanceof VBusiness) 
                {
                    datos[8] = "BUSINESS";
                }
                
                

                modelo.addRow(datos);
            }
        }
    }
