    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        String nombre = this.jTextField1.getText();
        String apellidos =this.jTextField2.getText();
        int DNI = Integer.parseInt(this.jTextField3.getText());
        String fechaNac = this.jTextField4.getText();
        String id = this.jTextField5.getText();
        String password = this.jPasswordField1.getText();
        String sexo = "";
        if(this.jRadioButton1.isSelected())
        {
            sexo = "MASCULINO";
        }
        else if(this.jRadioButton2.isSelected())
        {
            //problemas hasta el momento: no se guarda la informacion
            //sugerencias : pedirle al usuario que ingrese nuevamente la contraseña para que pueda confirmar que esta bien escrita.
            sexo = "FEMENINO"; 
        }
        Cliente obj = new Cliente(id,password,nombre,apellidos,DNI,fechaNac,sexo);
        obj.GenerarCodigoAleatorioCliente();
        refGPersona.ValidarDatos(obj);
        refGPersona.RegistrarNuevoUsuario(obj);
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

